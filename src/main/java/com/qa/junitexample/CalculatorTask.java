package com.qa.junitexample;

public class CalculatorTask {

	public static double add(int x, int y) {
		return x + y;

	}

	public static double sub(int x, int y) {
		return x - y;
	}
	
	public static double multi(int x, int y) {
		return x * y;

	}
	
	public static double divi(int x, int y) {
		return x / y;

	}


	public static double mod(int x, int y) {

		return x % y;
	}
}
